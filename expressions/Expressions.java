/**
* Expressions
*/
public class Expressions {

	public static void main(String[] args) {
		
	  /* Expression = operands & operators
	   * Operands = values, variables, numbers, quatity
	   * Operators = + - / % .etc
	   */

	  int apples = 10;
	  System.out.println(apples);

	  apples = apples + 1; //++ is the increment operator -- this should be written apples++; (-- decrement operator)

	  System.out.println(apples);
	}
}
